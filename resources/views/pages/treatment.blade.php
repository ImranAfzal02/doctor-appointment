<p><span class="sub-heading-bold">Skin sparing and nipples sparing mastectomy:</span>A surgery that removes the entire breast tissue, but preserves the skin, nipple, and areola, allowing for immediate or delayed breast reconstruction.</p>
<p><span class="sub-heading-bold">Skin reducing mastectomy:</span> A surgery that removes the entire breast tissue, nipple, and areola, along with some of the excess skin, and performs immediate breast reconstruction using an implant or a flap.</p>
<p><span class="sub-heading-bold">Breast conservation:</span> A surgery that removes only the part of the breast that contains the cancer, along with some surrounding normal tissue, and preserves the rest of the breast. It is also called a lumpectomy or a partial mastectomy.</p>
<p><span class="sub-heading-bold">Image guided breast conservation:</span> A surgery that uses imaging techniques, such as ultrasound or MRI, to guide the removal of the breast cancer and ensure clear margins.</p>
<p><span class="sub-heading-bold">Sentinel lymph node biopsy:</span> A procedure that removes the first few lymph nodes that receive lymph fluid from the breast and checks them for cancer cells. It helps determine the stage of the cancer and the need for further treatment.</p>
<p><span class="sub-heading-bold">Axillary node clearance:</span> A procedure that removes more lymph nodes from the armpit area to check for cancer spread and reduce the risk of recurrence. It is also called an axillary lymph node dissection.</p>

