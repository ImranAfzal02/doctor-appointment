<p class='services-heading'>BREAST CANCER DIAGNOSIS</p>
    <p>
    <span class="sub-heading-bold">X-ray mammogram:</span>
     An X-ray of the breast that can detect abnormal changes or masses in the breast tissue.
</p>

    <p>
        <span class="sub-heading-bold">Breast ultrasound:</span> 
        An imaging test that uses sound waves to create pictures of the breast and can help differentiate between solid and fluid-filled lumps.
</p>
<p>
    <span class="sub-heading-bold">MRI breast: </span>
    A magnetic resonance imaging test that uses magnetism and radio waves to produce detailed images of the breast and can help evaluate suspicious findings or assess the extent of breast cancer.
</p>
<p>
    <span class="sub-heading-bold">Ultrasound guided biopsy:</span> 
    A procedure that uses ultrasound to guide a needle into the breast and remove a sample of tissue for microscopic examination.
</p>
<p>
    <span class="sub-heading-bold">Ultrasound guided fine needle aspiration: </span>
    A type of biopsy that uses a thin needle to extract fluid or cells from a breast lump or cyst.
</p>
<p>
    <span class="sub-heading-bold">Ultrasound guided clip placement:</span> 
    A procedure that uses ultrasound to place a small metal marker inside the breast at the site of a biopsy or lumpectomy, so that the area can be easily identified on future imaging tests or surgery.
</p>
<p>
    <span class="sub-heading-bold">Ultrasound guided core localization:</span>
     A procedure that uses ultrasound to guide a wire into the breast and mark the location of a breast abnormality that needs to be removed by surgery.
</p>
<p>
    <span class="sub-heading-bold">Stereotactic core biopsy: </span>
    A type of biopsy that uses a computer and X-rays to locate a breast abnormality and guide a hollow needle to remove a core of tissue for analysis.
</p>
<p>
    <span class="sub-heading-bold">Stereotactic wire localization: </span>
    A procedure that uses a computer and X-rays to guide a wire into the breast and mark the location of a breast abnormality that needs to be removed by surgery
</p>
