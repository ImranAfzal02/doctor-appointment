    <p class="services-heading-8 mt-3">Breast cancer-associated LYMPHEDEMA prevention and treatment</p>

    <p class="services-heading-8 mt-4">Definition</p>
    <p>Accumulation of protein rich fluid in interstitial space due to disruption of lymphatic circulation.</p>

    <p>Lymphedema is a relatively common and often feared complication of breast cancer surgery</p>
    <p>Lymphedema is also associated with an increased incidence of recurrent
        episodes of cellulitis with potentially life-threatening consequences.</p>

    <p class="mt-5">Prevention and Early detection is key to management</p>


    <img src="{{ asset('images/lymphedema1.png') }}" alt="Image 1" class="img-fluid">

    <p class="mt-5 mb-4" style="font-weight:bold; text-decoration: underline">Incidence</p>
    <p>Highest incidence during 1st year Post-operative.</p>
    <p>20-50% CA Breast survivors eventually develop lymphedema</p>
    <p>80 % BCRL patients within first 3 years</p>
    <p>1% Life long risk per year</p>
    <p>Risk remains at 4-7% even after the shown success of SLNB</p>

    <p class="mt-5 mb-4" style="font-weight:bold; text-decoration: underline">Patho-physiology</p>
    <ul>
        <li>Essential for maintaining body’s homeostasis, immune response, transportation of nutrients, and
            waste products.</li>
        <li>Hydrostatic pressure in arteries pushes the fluid into the interstitial space to provide nutrients to
            the surrounding tissues.</li>
        <li>90% of interstitial fluid returns to circulation normally.</li>
        <li>Remaining 10% of interstitial fluid contains high molecular weight proteins ie large to enter
            capillaries.</li>
    </ul>

    <img src="{{ asset('images/lymph2.png') }}" alt="Image 2" class="img-fluid">


    <p class="mt-4">Lymphatic capillaries lack Basement Membrane therefore, they can absorb large particles.</p>
    <p>Lymphatic capillaries- Large collecting Vessels  They have Basement Membrane+ contractile
        pericytes and they possess valves which facilitate the lymph flow to Lymph Nodes  circulation</p>

    <ul>
        <li>When lymph vasculature is injured due to surgery, the vessels are unable to transport the protein
            rich fluid to/from interstitial space( accumulation of protein in the space)</li>
        <li>Increased oncotic pressure; draws additional water to interstitial space.</li>
    </ul>

    <p class="mt-4 mb-4" style="font-weight:bold;">Clinical Presentation</p>
    <p>Swelling with pitting oedema at this stage</p>
    <p>Prolonged stagnation of protein rich excess Interstitial fluid Leading to chronic inflammation and
        progression of lymphedema</p>

    <p>Eventually leads to the inflammatory cascade and fibrotic changes in lymphatic vessels and surrounding
        soft tissues.</p>


    <p>Accumulation of Interstitial Fluid activates the differentiation and proliferation of adipocytes.</p>

    <p>Leading to adipose deposits OR massive adipose tissue hypertrophy</p>

    <p>At the end increase in volume of extremity is noted by excess fat.</p>

    <p class="mt-4 mb-4" style="font-weight:bold;">Diagnosis</p>

    <p style="font-weight: bold;">Stage 0</p>
    <ul>
        <li>Swelling not evident</li>
        <li>Impaired Lymphatic drainage</li>
        <li> Changes in imaging</li>
    </ul>


    <p style="font-weight: bold;">Stage I</p>
    <ul>
        <li>Clearly visible soft tissue pitting edema</li>
        <li>Swelling reduced by elevating the arm</li>
    </ul>

    <p style="font-weight: bold;">Stage II</p>
    <ul>
        <li>Increased fibrosis, excess fat</li>
        <li>Pitting not detectable</li>
        <li>Swelling not reduced by elevation</li>
    </ul>



    <p style="font-weight: bold;">Stage III</p>
    <ul>
        <li>Extreme swelling</li>
        <li>Fibrotic hard tissues</li>
        <li>Non-pitting edema</li>
        <li>Skin changes (blisters etc)</li>
    </ul>


    <p class="mt-4 mb-4" style="font-weight:bold; text-decoration: underline">Risk Factors</p>
    <ul>
        <li>Extent of Surgery</li>
        <li>Extent of Local Surgery</li>
        <li>Radiation</li>
        <li>Obesity</li>
        <li>Genetic Susceptibility</li>
        <li>Infection</li>
    </ul>

    <p class="mt-4 mb-4" style="font-weight:bold; text-decoration: underline">Extent of Surgery</p>

    <p>Any kind of surgery ± radiation, has a risk of lymphedema</p>

    <p>ALND: strongly shows BCRL (all might not develop lymphedema)</p>
    <p>SNB: has reduced the incidence, but the risk still persists</p>

    <p>E.G Anatomical variations, the existence of alternative lymphatic pathways: Cephalic Pathway draining into suprclavicular Lymph Nodes.</p>
    <p>The tricipital pathway draining into posterior scapular nodes.</p>

    <p class="mt-4 mb-4" style="font-weight:bold; text-decoration: underline">Extent of Local Surgery</p>
    <p>Mastectomy: Increased risk of lymphedema</p>
    <p>BCS Decreased risk of lymphedema</p>


    <p class="mt-4 mb-4" style="font-weight:bold; text-decoration: underline">Radiation</p>
    <p>Exact mechanism is not known but, it has been suggested that XRT Induced soft tissue fibrosis contributes to lymphedema</p>
    <p>It also decreases the number of  dermal capillary lymphatic vesseles and lymphatic  endothelial  cells, further adding to dysfunction.</p>

    <p class="mt-4 mb-4" style="font-weight:bold; text-decoration: underline">Obesity</p>
    <ul>
        <li>Being over weight at the time of BCA Diagnosis is also one of the reasons for lymphedema</li>
        <li>BMI > 30 kg/m 2  has nearly 3 folds increased risk of lymphedema as compared to BMI < 25kg/m 2</li>
        <li>Obesity itself impairs the lymphatic flow</li>
    </ul>

    <p class="mt-4 mb-4" style="font-weight:bold; text-decoration: underline">Infection</p>
    <p>Lymphatic drainage is important for immunity, damage to this weakens the protection against infections leading to lymphedema.</p>
    <p class="mt-4 mb-4" style="font-weight:bold; text-decoration: underline">Signs and Symptoms</p>
    <p class="mt-4 mb-4" style="font-weight:bold; text-decoration: underline">Early Signs</p>
    <ul>
        <li>Slight puffiness</li>
        <li>Feeling of fullness</li>
        <li>Indentation from jewellery</li>
        <li>Swelling might subside during night</li>
    </ul>

    <p class="mt-4 mb-4" style="font-weight:bold; text-decoration: underline">Persistant Symptoms</p>
    <ul>
        <li>Chronic lymphedema</li>
        <li>Pitting not detectable</li>
        <li>Size discrepancy of the arms</li>
    </ul>

